
public class Number1 {

	/**
	 * Find the sum of all the multiples of 3 or 5 below 1000.
	 */
	public static void main(String[] args) {
		int sum = 0;
		int count = 1;
		while (count < 1000)
		{
			if (count % 3 == 0 )
			{
				sum = sum + count;
				count++;
			}
			else if (count % 5 == 0)
			{
				sum = sum + count;
				count++;
			}
			else
				count++;
			}
		System.out.println(sum);
	

	}

}
